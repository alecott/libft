#include "libft.h"

char	ft_human_metric_bin(size_t nb)
{
	if (nb < 1024)
		return ('B');
	if (nb < 1048576)
		return ('K');
	if (nb < 1073741824)
		return ('G');
	if (nb < 1099511627776)
		return ('T');
	if (nb < 1125899906842624)
		return ('P');
	if (nb < 1152921504606846976)
		return ('E');
	if (nb >= 1152921504606846976)
		return ('Z');
	return (0);
}
