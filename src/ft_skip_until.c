#include "libft.h"

void		ft_skip_until(char **str, char c)
{
	char	*s;

	if (str == 0 || *str == 0)
		return ;
	s = *str;
	while (*s && *s != c)
	{
		if (*s == '\\')
			s ++;
		s++;
	}
	*str = s;
}
